public class soal3 {
     public static void main(String[] args) {
        // Mendefinisikan karakter 'X' dan 'y'
        char charX = 'X';
        char charY = 'y';
        
        // Mengambil kode ASCII dari karakter 'X' dan 'y'
        int asciiX = (int) charX;
        int asciiY = (int) charY;
        
        // Menjumlahkan kode ASCII dari karakter 'X' dan 'y'
        int sum = asciiX + asciiY;
        
        // Menampilkan hasil
        System.out.println("Jumlah ASCII dari karakter 'X' dan 'y' adalah: " + sum);
    }
}
